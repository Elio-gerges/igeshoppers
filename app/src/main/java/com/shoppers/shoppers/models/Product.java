package com.shoppers.shoppers.models;

public class Product {
    private String name;
    private String img;

    public Product() {}

    public Product(String name, String img) {
        this.name = name;
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }
}
